package excelcreator;

/**
 *
 * @author default
 */
public class WriteTable {
    private String A;
    private String B;
    private int C;
    private int D;
    private int E;

    public WriteTable() {
    }

    public WriteTable(String A, String B, int C, int D, int E) {
        this.A = A;
        this.B = B;
        this.C = C;
        this.D = D;
        this.E = E;
    }

    /**
     * @return the A
     */
    public String getA() {
        return A;
    }

    /**
     * @param A the A to set
     */
    public void setA(String A) {
        this.A = A;
    }

    /**
     * @return the B
     */
    public String getB() {
        return B;
    }

    /**
     * @param B the B to set
     */
    public void setB(String B) {
        this.B = B;
    }

    /**
     * @return the C
     */
    public int getC() {
        return C;
    }

    /**
     * @param C the C to set
     */
    public void setC(int C) {
        this.C = C;
    }

    /**
     * @return the D
     */
    public int getD() {
        return D;
    }

    /**
     * @param D the D to set
     */
    public void setD(int D) {
        this.D = D;
    }

    /**
     * @return the E
     */
    public int getE() {
        return E;
    }

    /**
     * @param E the E to set
     */
    public void setE(int E) {
        this.E = E;
    }
    
    
}
